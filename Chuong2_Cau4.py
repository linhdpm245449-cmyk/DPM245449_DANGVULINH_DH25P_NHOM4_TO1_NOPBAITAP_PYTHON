#Câu 4: Python hỗ trợ những kiểu dữ liệu cơ bản nào?
"""
Số nguyên (int):

Là kiểu dữ liệu dùng để lưu trữ các số nguyên (không có phần thập phân).

Số thực (float):

Dùng để lưu trữ các số có phần thập phân.

Chuỗi ký tự (str):

Dùng để lưu trữ văn bản, có thể là một hoặc nhiều ký tự.

Boolean (bool):

Dùng để lưu trữ giá trị đúng hoặc sai. Chỉ có hai giá trị: True và False.

Danh sách (list):

Là một tập hợp có thứ tự các phần tử, có thể chứa các kiểu dữ liệu khác nhau.

Tuple:

Tương tự như danh sách nhưng không thể thay đổi (immutable).

Từ điển (dict):

Dùng để lưu trữ dữ liệu dưới dạng cặp khóa-giá trị.

Tập hợp (set):

Là một tập hợp các phần tử duy nhất, không có thứ tự.

None:

Là giá trị đặc biệt biểu thị sự vắng mặt của giá trị, tương tự như null trong các ngôn ngữ khác.
"""