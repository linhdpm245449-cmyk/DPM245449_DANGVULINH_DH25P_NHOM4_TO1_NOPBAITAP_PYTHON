"""
1. Toán tử chia /:

Mục đích: Thực hiện phép chia và luôn trả về kết quả dưới dạng số thực (float), kể cả khi chia hết.

Cú pháp: a / b
2. Toán tử chia lấy phần nguyên //:

Mục đích: Thực hiện phép chia và chỉ lấy phần nguyên của kết quả. Phần dư sẽ bị bỏ qua.

Cú pháp: a // b
3. Toán tử chia lấy phần dư %:

Mục đích: Trả về phần dư khi chia hai số.

Cú pháp: a % b
4. Toán tử lũy thừa **:

Mục đích: Tính lũy thừa, tức là lấy một số mũ của một số khác.

Cú pháp: a ** b
5. Toán tử and:

Mục đích: Toán tử logic "và". Trả về True chỉ khi cả hai điều kiện đều đúng.

Cú pháp: a and b
6. Toán tử or:

Mục đích: Toán tử logic "hoặc". Trả về True nếu ít nhất một trong các điều kiện là đúng.

Cú pháp: a or b
7. Toán tử is:

Mục đích: Kiểm tra sự đồng nhất (identity) của hai đối tượng. Nó sẽ trả về True nếu hai đối tượng có cùng vị trí bộ nhớ, tức là chúng là một đối tượng duy nhất.

Cú pháp: a is b
"""